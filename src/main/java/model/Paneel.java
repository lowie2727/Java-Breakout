public class Paneel {

    private double breedte;
    private double hoogte;

    public Paneel() {
        breedte = 1000;
        hoogte = 500;
    }

    /**
     * @return the breedte
     */
    public double getBreedte() {
        return breedte;
    }

    /**
     * @return the hoogte
     */
    public double getHoogte() {
        return hoogte;
    }

}
